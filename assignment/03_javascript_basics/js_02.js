// Introduction to Variables

/* 
Variables can store information.
We can use that information later or
we can change (update) that information.
*/

// Declare a variable:
var name = "suraj";

// Use the variable:
console.log(name); // Output: suraj

// Change the variable:
var name = "bhaiya"; // Re-declaring and assigning a new value (allowed with var)
console.log(name); // Output: bhaiya
