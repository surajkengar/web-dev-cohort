// declare constant

// We declare a constant using the 'const' keyword.
const pi = 3.14;
console.log(pi); // Output: 3.14

// Once we declare a constant and initialize it, 
// we CANNOT reassign or redeclare it.
const gravity = 9.8;
// gravity = 10; // Error: Assignment to constant variable

