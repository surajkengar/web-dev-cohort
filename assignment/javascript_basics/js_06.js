// String: A string is a sequence of characters.

// String Indexing: Indexing starts from 0.
// Example:  "s u r a j"
//           "0 1 2 3 4"

// Accessing characters in a string:
let name = "suraj"; // This is a string
console.log(name[2]); // Output: 'r' (3rd letter because index starts at 0)

// Finding the length of a string using the .length property:
let first_name = "suraj";
console.log(first_name.length); // Output: 5

// Accessing the last character of the string:
let game = "cricket";
console.log(game[game.length - 1]); // Output: 't'
