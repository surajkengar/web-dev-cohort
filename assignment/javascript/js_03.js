// Rules for Defining Variables in JavaScript:

// 1 Variable names cannot start with a number:
// var 1demo;      // Invalid
// var demo2;      //  Valid

// 2️ Only $ (dollar) and _ (underscore) special characters are allowed:
// var _firstname; //  Valid
// var first_name; //  Valid (snake_case)
// var first$name; //  Valid
// var $firstname; //Valid

// 3️ Variable names cannot contain spaces:
// var first name = "suraj";  //  Invalid
// var first_name = "suraj";  //  Valid (snake_case)
// var firstName = "suraj";   //  Valid (camelCase)

// Naming Conventions:
// - By convention, variable names start with a lowercase letter.
// - Use camelCase for multiple words (firstName, lastName).
// - Snake_case is also allowed but less common in JS.

// Examples:
// let myVariableName = "camelCase";  //  Common JS style
// let my_variable_name = "snake_case"; //  Allowed but less common
